export function getVetBySlug(slug: string): VetRecord | undefined {
  return VETS.find((v) => v.slug === slug);
}

export function getFeaturedVets(): VetRecord[] {
  return VETS.filter((v) => v.featured);
}

export function getVetsByState(stateSlug: string): VetRecord[] {
  return VETS.filter((v) => v.stateSlug === stateSlug);
}

// citySlug helper for matching city names to URL slugs
function citySlug(c: string): string {
  return c.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "");
}

/**
 * Get all vets in a specific city (matched by slug).
 */
export function getVetsByCity(stateSlug: string, citySlugParam: string): VetRecord[] {
  return VETS.filter(
    (v) =>
      v.stateSlug === stateSlug &&
      citySlug(v.city) === citySlugParam
  );
}