export function getBreedersByState(abbr: string): BreederRecord[] {
  return BREEDERS.filter((b) => b.state === abbr);
}

export function getBreederSlugs(): string[] {
  return BREEDERS.map((b) => b.slug);
}

function citySlug(c: string): string {
  return c.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "");
}

/**
 * Get all breeders in a specific city (by city slug) within a state.
 * Matches by reversing the city slug against each breeder's city name.
 */
export function getBreedersByCity(stateAbbr: string, citySlugParam: string): BreederRecord[] {
  return BREEDERS.filter(
    (b) => b.state === stateAbbr && citySlug(b.city) === citySlugParam
  );
}

/**
 * Returns the city slugs for the state's `topCities` list, in array order.
 * Used by generateStaticParams. Does NOT filter by breeder count — that's
 * the page's job (it 404s on zero-breeder cities).
 */
export function getCitySlugsForState(stateAbbr: string): string[] {
  const state = getStateByAbbr(stateAbbr);
  if (!state) return [];
  return state.topCities.map(citySlug);
}